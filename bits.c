/*
 * isNotEqual - return 0 if x == y, and 1 otherwise
 *   Examples: isNotEqual(5,5) = 0, isNotEqual(4,5) = 1
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 6
 *   Rating: 2
 */
int isNotEqual(int x, int y) {
  /*
   * if x == y, !(x ^ y) returns 1; otherwise, return 0;
   * So if x == y, a = 0x10000000; otherwise, a = 0x11111111;
   * the least bit of a completes the trick
   * Hence (a << 31) >>31 returns 0 if x == y, returns 1 otherwise
   */
  unsigned a = ~(!(x ^ y));
  return  (a << 31) >> 31;
}
