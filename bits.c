/*
 * bang - Compute !x without using !
 *   Examples: bang(3) = 0, bang(0) = 1
 *   Legal ops: ~ & ^ | + << >>
 *   Max ops: 12
 *   Rating: 4
 */
int bang(int x) {
  /* ! operation returns 0 for any non-zero numbers
   * the most significnat bit of a number determines whether it is positive or negative
   * therefore, for any non-zero number, 
   * the sign bit of the orignial number and its logical negative is either 0 or 1
   */

  int neg_x = ~x + 1; //get the negative value of x;

  /* arithmetic shift of the most singnificant bit of (x|neg_x)
   * the reuslt is either 1 or 0
   */
  int signBit = (x | neg_x) >> 31; 

  // If x == 0, signBit = 0, return 1; if x !== 0, signBit = -1, return 0;
  return signBit + 1;
}
/*
 * bitXor - x^y using only ~ and &
 *   Example: bitXor(4, 5) = 1
 *   Legal ops: ~ &
 *   Max ops: 14.
 *   Rating: 1.
 */
int bitXor(int x, int y) {
  /*
   * ~(x & y) returns 0 if x and y has 1 on the same bit
   * ~((~x) & (~y)) returns 0 if x and y has 0 on the same bit
   * thereore, & the above results will complete the ^ operation
   */
  return (~(x & y)) & (~((~x) & (~y)));
}
/*
 * conditional - same as x ? y : z
 *   Example: conditional(2,4,5) = 4
 *   Legal ops: ! ~ & ^ | + << >>
 *   Max ops: 16
 *   Rating: 3
 */
int conditional(int x, int y, int z) {
 
  /*
   * Logic: When x == 0, return y; when x !== 0, return z;
   * When x == 0, ((!x) << 31) >> 31 = 0x11111111; otherwise, ((!x) << 31) >> 31 = 0xffffffff;
   * 0xffffffff & an integer return the itself, while 0x00000000 & an integeger return 0x00000000;
   */

  int leastBit = ((!x) << 31) >> 31; // return 0x00000000 or 0xffffffff;
  int a = ~leastBit; // return 0xfffffffff or 0x00000000;
  return ((a & y) + (leastBit & z)); 
}
